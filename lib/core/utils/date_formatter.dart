/// Utility functions for formatting and displaying dates
class AppDateUtils {
  /// Formats a DateTime to display relative time (e.g., "2 hours ago")
  static String formatTimeAgo(DateTime dateTime) {
    final now = DateTime.now();
    final difference = now.difference(dateTime);

    if (difference.inDays > 7) {
      return '${dateTime.day}/${dateTime.month}/${dateTime.year}';
    } else if (difference.inDays > 0) {
      return '${difference.inDays} วันที่แล้ว';
    } else if (difference.inHours > 0) {
      return '${difference.inHours} ชั่วโมงที่แล้ว';
    } else if (difference.inMinutes > 0) {
      return '${difference.inMinutes} นาทีที่แล้ว';
    } else {
      return 'เมื่อสักครู่';
    }
  }

  /// Alias for formatTimeAgo for backward compatibility
  static String formatRelativeTime(DateTime dateTime) {
    return formatTimeAgo(dateTime);
  }

  /// Formats a DateTime to a readable date string
  static String formatDate(DateTime dateTime) {
    const months = [
      'มกราคม', 'กุมภาพันธ์', 'มีนาคม', 'เมษายน', 'พฤษภาคม', 'มิถุนายน',
      'กรกฎาคม', 'สิงหาคม', 'กันยายน', 'ตุลาคม', 'พฤศจิกายน', 'ธันวาคม'
    ];
    
    return '${dateTime.day} ${months[dateTime.month - 1]} ${dateTime.year + 543}';
  }
}
